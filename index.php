<p><b>The civil Parish of Shere is the largest in area in the Borough of Guildford, Surrey.</b></p>

<p>The Parish lies in the Green Belt, the Surrey Hills Area of Outstanding Natural Beauty and an Area of Great Landscape Value.&nbsp;
