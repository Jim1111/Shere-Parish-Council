<div class="cushycms" title="About Shere Parish Council">

<p ALIGN="JUSTIFY">Parish Councils were
created as a result of the Local Government Act of 1894. Generally they got off
to a slow start and only really began to exercise their power following the end
of the first world war. Parish Councils are the first level of local democracy,
right at the grass roots so to speak. They can only act in relation to the
powers that have been conferred by Acts of Parliament.</p>
<p ALIGN="JUSTIFY">The Parish of Shere is easily the largest in area in
Guildford Borough. It is fairly unusual in that where most Parish Councils serve
only one or two villages, Shere Parish Council serves 4.5 villages these being
Gomshall, Holmbury St. Mary, Peaslake, Shere and a large part of Abinger Hammer
(excluding the village centre).</p>

<p ALIGN="JUSTIFY">The Parish Council is responsible for administering local
affairs</b>. Approximately ten full Council meetings are held each year in
addition to Finance, Health and Safety, Parish Maintenance, Planning, Playground
and Tanyard Hall Committee meetings.</p>

<p ALIGN="JUSTIFY">All meetings are open to the public and there is an
adjournment at every Council and Committee meeting to enable the public to
comment on any item on the agenda or to address the Council. In addition, in
order for the Parish Council to be fully effective it would be&nbsp;pleased if
residents would contact the Council office if they have any issues, concerns or
suggestions relating to the Parish </p>
<b>
<p ALIGN="JUSTIFY">Elections for the Council are held every four years.</p>
<p ALIGN="JUSTIFY">The Parish Council is also an important avenue for the
expression of local public opinion to other authorities.</b></p>

<p ALIGN="JUSTIFY">Planning Applications - </b>The Parish Council is a
statutory consultee for all planning applications which means that Guildford
Borough Council have to consult the Parish Council on planning applications in
the Parish. A Parish Council Planning Committee meeting is held approximately
every two to three weeks and the Parish Council’s representations are then
forwarded to the planning authority.</p>

<p ALIGN="JUSTIFY">The Council either owns or leases the following property:</p>
<p ALIGN="JUSTIFY"><strong>Gomshall - Tanyard Hall, Gomshall</strong> </b>- available for hire
(please see <a href="tanyard_hall.php">separate item</a>), two recreational grounds and a large playground at
Goose Green, a playground and recreational area at Tower Hill Rise.</p>


<p ALIGN="JUSTIFY"><strong>Holmbury St. Mary</b></strong> - The Fir Pound, Felday Field and
adjacent play area, Holmbury Football pitch, car park adjacent to the village
hall, the village pump and surrounding area/small car park.</p>

<p ALIGN="JUSTIFY"><strong>Peaslake</strong><strong></b></strong> - Recreational field and play area near
Peaslake School, the War Memorial and the bus shelter area in the centre of the
village, Pond Lane car park, Peaslake Farm. The bank of the stream along the
Ewhurst Road from the village centre to Peaslake Farm. </p>

<p ALIGN="JUSTIFY"><b>Shere</b> - The Pound, The Old Fire Station and garden, the
Square, the bank of the stream in Lower Street, the area around the War
Memorial, the Play area on the Shere Recreation Ground, The Swimming Pool field
and the pool itself which is older than the Parish Council! The Parish Council
also operates the public toilets in the Old Fire Station in Middle Street.</p>

<p ALIGN="JUSTIFY">In addition the Parish Council owns most of the bus shelters,
seats, village signs and other street furniture in the villages and maintains
these during the year plus other smaller areas of land throughout the Parish.</p>

<p ALIGN="JUSTIFY">For many, many years the Parish Council has contributed 50%
of the cost of maintaining the three cemeteries in the Parish that are situated
in the villages of Holmbury St. Mary, Peaslake and Shere. It also maintains the
grounds of the Felday Chapel in Holmbury St. Mary.</p>

<b>
<p ALIGN="JUSTIFY">The Council is made up of thirteen Councillors</b> - full
details can be found on the website under the heading - "<a href="Councillors.php">Councillors</a>".
The Clerk of the Council works from the Council offices at Tanyard Hall on a
daily basis.</p>

<b>
<p ALIGN="JUSTIFY">Quality Status</b> - Shere Parish Council obtained Quality Council status
in 2004 (4 year term) and this was recredited for a further 4 years in 2008. Quality
Council status is awarded to a Council that:</p>




<blockquote>
    <p ALIGN="JUSTIFY">is effectively and properly managed and articulates the
    needs and wishes of its community;</p>
    <p ALIGN="JUSTIFY">is representative of, and actively engages, all parts of
    its community, providing vision, identity and a sense of belonging;</p>
    <p ALIGN="JUSTIFY">is committed to work in partnership with principal
    authorities and other public service agencies;</p>
    <p ALIGN="JUSTIFY">in proportion to size and skills, delivers local services
    on behalf of principal authorities when this represents the best deal for
    the community;</p>
    <p ALIGN="JUSTIFY">acts as an information point for local residents.</p>
</blockquote>

